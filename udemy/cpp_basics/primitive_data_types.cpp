/* fundamental data types are */
/*
 * expressed in bits
 * the more bits the more values
 * the more bits the more storage req'd
 */

/* CHARS */
/*
 * char      1 byte
 * char16_t  2 bytes
 * char32_t  4 bytes
 * wchar_t   represent the largest
 */

/* INTS */
/*
 * signed short int         2 bytes
 * signed int               2 bytes
 * signed long int          4 bytes
 * signed long long int     8 bytes
 * Same bytes for unsigned
 */
